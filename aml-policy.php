            <p>Ongoing monitoring</p>
            <p>What is ongoing monitoring?</p>
            <p>Ongoing monitoring is an intrinsic part of the CDD process. It must be performed on all matters, regardless of their individual risk rating, in order to detect unusual or suspicious transactions.</p>
            <p>How do I conduct ongoing monitoring?</p>
            <p>You should:</p>
            <ul class="pl-5" style="list-style-type:disc">

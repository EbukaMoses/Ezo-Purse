            <h1 class="first-title">AML Policy</h1>
            <h5 class="mt-3 pb-3">INTRODUCTION TO THE POLICY</h5>
            <p>CyberExchange is required under the Money Laundering Regulations 2017 to put in place appropriate systems and controls to forestall money laundering and terrorist financing. This policy contains the procedures that we have developed in order to comply with these obligations.</p>

            <p>The Money Laundering Regulations require that an organisation has a Nominated Officer to ensure that there is up-to-date knowledge of issues relating to Anti-Money Laundering and Counter-Terrorist Financing throughout the organisation, implement appropriate policies and procedures and receive reports of suspicious activity. The Nominated Officer (Money Laundering Reporting Officer) for CyberExchange is Nosa Owenaze.</p>
            
            <h5 class="font-weight-bold pt-3  pb-2 text-black">WHAT IS MONEY LAUNDERING AND TERRORIST FINANCING?</h5>
            <p>Money laundering is the process through which proceeds of crime and their true origin and ownership are changed so that the proceeds appear legitimate. Terrorist financing is providing or collecting funds, from legitimate or illegitimate sources, to be used to carry out an act of terrorism.</p>

            <h5 class="font-weight-bold pt-3  pb-2 text-black">WHY IS ANTI-MONEY LAUNDERING AND COUNTER-TERRORIST FINANCING IMPORTANT TO CYBEREXCHANGE?</h5>
            <p>Lawyers facilitate significant transactions and are gatekeepers to the legal system. The anti-money laundering (AML) and counter-terrorist financing (CTF) regime is designed to prevent our services being used by criminals. You have obligations under the AML/CTF regime to spot and report money laundering and terrorist financing. Failure to meet these obligations can lead to criminal penalties, substantial fines and untold damage to your own and CyberExchange’s reputation.</p>

            <h5 class="font-weight-bold pt-3  pb-2 text-black">HOW DOES MONEY GET LAUNDERED?</h5>
            <p>Typically money laundering involves three stages:</p>
            <p>Placement:</p>
            <p>The process of placing criminal property into the financial system. This might be done by breaking up large sums of cash into smaller amounts or by using a series of financial instruments (such as cheques or money orders) which are deposited at different locations.</p>
            <p>Layering:</p>
            <p>The process of moving money that has been placed in the financial system in order to obscure its criminal origin. This is usually achieved through multiple complex transactions often involving complicated offshore company structures and trusts.</p>
            <p>Once the origin of the money is disguised it ultimately must reappear in the financial system as legitimate funds. This process involves investing the money in legitimate businesses and other investments such as property purchases or setting up trusts.</p>
            <p>We are most likely to become involved in the layering stage but potentially could be involved in any stage.</p>
            <h5 class="font-weight-bold pt-3  pb-2 text-black">HOW DO I KNOW IF MY MATTER INVOLVES MONEY LAUNDERING OR TERRORIST FINANCING?</h5>
            <p>You do not have to behave like a police officer but you do have to remain alert to the warning signs of money laundering and terrorist financing and make the sort of enquiries that a reasonable person (with the same qualifications, knowledge and experience as you) would make.</p>
            <p>Typical signs of money laundering and terrorist financing are:</p>
            <ul style="list-style-type:disc">
                <li>Obstructive or secretive clients</li>
                <li> Instructions outside our usual range of expertise, i.e. why is the client using us?</li>
                <li> Clients based a long way from us with no apparent reason for using us</li>
                <li> Cases or instructions that change unexpectedly or for no logical reason, especially where:</li>
                <li>The client has deposited funds with us</li>
                <li>The source of funds changes at the last moment</li>
                <li>You are asked to return funds or send funds to a third party</li>
                <li>Loss-making transactions where the loss is avoidable</li>
                <li>Complex or unusually large transactions</li>
                <li>Transactions with no apparent logical, economic or legal purpose</li>
                <li>Large amounts of cash being used</li>
                <li>Money transfers where there is a variation between the account holder and signatory</li>
                <li>Payments to or from third parties where there is no logical connection to the client</li>
                <li> Movement of funds between accounts, institutions or jurisdictions without reason</li>
                <li>Retainers involving high risk jurisdictions (e.g. Iran, Uzbekistan, Turkmenistan, Pakistan, Sao Tome and Northern Cyprus)</li>
                <li>Large payment on account of fees with instructions terminated shortly after and the client requesting the funds are returned</li>
            </ul>
            <p>Criminals are always developing new techniques so this list can never be exhaustive.</p>
            <h5 class="font-weight-bold pt-3  pb-2 text-black">WHAT IS SUSPICIOUS ACTIVITY?</h5>
            <p>Any client activity outside the normal or expected activity should be considered unusual and must be investigated. Understanding the business or client profile is crucial. Unusual activity or transactions outside the established profile should be considered as a potential indicator of suspicious activity. Investigations should establish the reasons for the unusual activity or transaction. This may either remove or confirm your suspicion. If it is confirmed, you must report it to the MLRO. Failure to do so is an offence that could result in five years imprisonment.</p>
            <p>What to do if you have a suspicion?</p>
            <p>Report it to your MLRO. Do not carry out the transaction or proceed unless you have consent from the MLRO. They will review the suspicion and, if required, submit a Suspicious Activity Report (SAR) to the National Crime Agency (NCA). Only the MLRO or deputy may submit an SAR to the NCA. Once you have reported your suspicion to the MLRO, they will send you an acknowledgement within 24 hours. If more information is required, the MLRO will request it from you.</p>
            <p>If the MLRO gives you consent to proceed with a transaction, then that consent only applies to that specific transaction. If the client requests further activities or transactions, further consent is required from the MLRO even if you do not have a suspicion.</p>
            <p>SAR</p>
            <p>This is a suspicious activity report which financial institutions must make if they suspect something in a transaction is illegal. Law enforcement will make a decision after a SAR has been submitted. If no response has been received seven working days after the SAR was submitted, then the transaction can proceed. It may be a tipping off offence to reveal to the customer that a SAR has been submitted. A SAR should be submitted within 48 hours of a suspicion being formed.</p>
            <p>Information that a SAR has been made should never be placed on a client file.</p>
            <p>Tipping off</p>
            <p>In most jurisdictions it is an offence for someone to tip off (inform) a person suspected of money laundering that a Suspicious Activity Report (SAR) has been made or there is a money laundering investigation taking place. There are a number of defences and exceptions that apply, but in general a tipping off offence would occur when the action is likely to prejudice an investigation that’s taking place.</p>
            <p>A tipping off offence cannot be committed if a report has not been submitted and you liaise with clients or colleagues as part of your enquiries into an unusual activity. However, you cannot mention the word suspicious.</p>
            <p>Money Laundering Offences</p>
            <p>The Proceeds of Crime Act 2002 (POCA 2002) establishes a number of money laundering offences:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>The principal offences</li>
                <li>Failure to disclose offences</li>
                <li>The offences of tipping-off and prejudicing an investigation</li>
                
            </ul>
            <p>Each offence is explained below. All money laundering offences relate to criminal property, which is property that constitutes or represents a person’s benefit:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li> In whole or in part</li>
                <li>From criminal conduct</li>
                <li>Whether directly or indirectly</li>
            </ul>
            <p>This definition covers the proceeds of all crimes. There is no minimum limit on what is considered to be criminal property.</p>
            <p>Criminal conduct is all conduct that constitutes an offence in any part of the Nigeria or overseas.</p>
            <p>The principal Offences</p>
            <p>You will commit a principal money laundering offence if you:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Conceal, disguise, convert, transfer or remove criminal property from Nigeria (s327)</li>
                <li>Enter into or become concerned in an arrangement which facilitates the acquisition, retention, use or control of criminal property for or on behalf of another (s328), or</li>
                <li>Acquire, use or have possession of criminal property (s 329)</li>
            </ul>
            <p>Concealing (s327)</p>
            <p>You will commit an offence if you:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Conceal</li>
                <li>Disguise</li>
                <li>Convert</li>
                <li>Transfer</li>
                <li>Remove from the Nigeria</li>
            </ul>
            <p>This includes concealing or disguising its:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Nature</li>
                <li>Source</li>
                <li>Location</li>
                <li>Disposition</li>
                <li>Movement</li>
                <li>Ownership</li>
                
            </ul>
            <p>You must know or suspect that the criminal property represents a benefit from criminal conduct.</p>
            <p>Acquisition (s329)</p>
            <p>You will commit an offence if you:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Acquire</li>                
                <li>Use</li>                
                <li>Have possession of</li>                
            </ul>
            <p>Possession means having physical custody of the criminal property. The principal money laundering offences carry a maximum penalty of 14 years’ imprisonment, a fine or both. You will have a defence to a principal money laundering offence if you submit a Suspicious Activity Report (SAR) to Nosa Owenaze</p>
            <p>Failure to report</p>
            <p>Making an SAR to the Nominated Officer can be a defence to a principal money laundering offence.</p>
            <p>Failing to make a SAR to the Nominated Officer where you know or suspect money laundering is an offence in itself which is punishable by up to five years’ imprisonment, a fine or both.</p>
            <p>See further Reporting suspicions below.</p>
            <p>Tipping-off and prejudicing an investigation</p>
            <p>You will commit the tipping-off offence if you disclose to the person to whom the disclosure relates that you, or anyone else:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Has made an SAR to the Nominated Officer (or NCA)</li>                
                <li>Of information which came to you in the course of business</li>                
                <li>That disclosure is likely to prejudice any investigation that might be conducted following the SAR</li>                
            </ul>
            <p>You will commit the prejudicing an investigation offence if you disclose that an investigation is being contemplated or carried out and that disclosure is likely to prejudice that investigation. Further, you will commit an offence if you know or suspect that an investigation is being or is about to be conducted and you interfere with documents which are relevant to the investigation. Tipping-off can only be committed after an SAR (including an internal SAR to Nosa Owenaze) has been made. You will not commit tipping-off by discussing your concerns with or submitting a SAR to the Nosa Owenaze.</p>
            <p>All these offences are punishable by up to five years’ imprisonment, a fine or both. The existence of these offences does not prevent you from making normal enquiries about your clients’ instructions. You are able to make enquiries in order to:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Obtain further information to help you decide whether you have a suspicion, and/or</li>               
                <li>Remove any concerns that you have</li>               
            </ul>
            <p>Your enquiries will only constitute an offence if you disclose that an SAR has been made or that an investigation is being carried out or contemplated. It is also not tipping-off to warn your clients of your duties under the AML/CTF regime by providing them with our terms of business or our standard client care letter.</p>
            <p>Our internal SAR form can be found at Appendix 1. Any member of staff can submit a SAR form to the Nominated Officer.</p>
            <p>Terrorist Financing Offences</p>
            <p>Terrorists need funds to plan and carry out attacks. The Terrorism Act 2000 (TA 2000) criminalises both participation in terrorist activities and terrorist financing.</p>
            <p>In general terms, terrorist financing is:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>The provision or collection of funds</li>              
                <li> From legitimate or illegitimate sources</li>              
                <li>With the intention or in the knowledge</li>              
                <li>That they should be used in order to carry out any act of terrorism</li>              
                <li> Whether or not those funds are in fact used for that purpose</li>              
                        
            </ul>
            <p>The TA 2000 establishes a similar pattern of offences to those contained in POCA 2002, i.e:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Principal terrorism offences of:</li>
                <li>Fundraising</li>
                <li> Use or possession</li>
                <li>Arrangements</li>
                <li>Money laundering</li>
                <li> Failure to disclose offences</li>
                <li> Tipping-off offences</li>
            </ul>
            <p>All offences carry heavy criminal penalties. While the terrorist financing and money laundering regimes are different, they share similar aims and structures and run together in Nigeria legislation. Many of the provisions of POCA 2002 and TA 2000 mirror one another and the definitions are deliberately matched.</p>
            <p>Both POCA 2002 and TA 2000 run parallel to the Money Laundering Regulations 2007 (Amended 2012), which are explained below.</p>
            <p>The Money Laundering Regulations 2017</p>
            <p>The Money Laundering Regulations 2017 set administrative requirements which require us to have systems and controls to forestall money laundering and terrorist financing. They implement the standards of the Fourth European Anti-Money Laundering Directive into Nigeria law.</p>
            <p>Client Due Diligence (CDD)</p>
            <p>Client Due Diligence is:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Identifying and verifying the client’s identity</li>
                <li> Identifying the beneficial owner where this is not the client</li>
                <li>Obtaining details of the purpose and intended nature of the business relationship</li>
                <li>Conducting ongoing monitoring of the business relationship</li>
                
            </ul>
            <p>When do I have to conduct CDD?</p>
            <p>You must carry out CDD:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Before you establish a business relationship with a client</li>
                <li>Before you carry out a one-off transaction for a client including company formation</li>
                <li>Where there is reason to believe that CDD carried out on an existing client is inadequate</li>
                <li>Where the client’s identifying details (e.g. name and address) have changed</li>
                <li>Where the client has not been in regular contact with us</li>
                <li>Where someone is purporting to act on behalf of a client</li>
                <li>Where you suspect money laundering or terrorist financing</li>
            </ul>
            <p>You must also identify the beneficial owner and verify them, but not solely based on Companies House register of beneficial ownership. You must obtain and verify the names of the body corporate, its registration number, registered address and principal place of business. Reasonable measures must also be taken to determine and verify the law to which it is subject, its constitution and the names of its board of directors and senior management.</p>
            <p>How do I conduct CDD?</p>
            <p>You must start with assessing the risk of money laundering or terrorist financing posed by the client and complete a risk assessment (Appendix 3). Once this is complete, you must decide what level of CDD is necessary. This will then inform your next steps.</p>
            <p>Risk assessment</p>
            <p>Cardvest has risk assessed various types of client – see the CDD Risk Table (Appendix 6.3). This will help you to complete your CDD Risk Assessment Form (Appendix 6.4). Once you have completed your risk assessment, you will be able to decide what level of CDD to apply, i.e. enhanced, simplified or regular. The specific CDD measures that you must then apply and the documents that you must obtain in each case are set out in “Identify and verify – what information or documents do I need to obtain from my client?” below.</p>
            <p>It is your responsibility to check the accuracy and adequacy of the documents provided. If you are in any doubt please contact Nosa Owenaze.</p>
            <p>Simplified Due Diligence (SDD)</p>
            <p>Simplified Due Diligence applies where there is little chance of money laundering or terrorist financing. This means that we can carry out a reduced Client Due Diligence exercise, which simply involves obtaining evidence of why SDD applies. For example, where SDD applies to a company listed on the Nigeria Stock Exchange you will need to obtain evidence of the company’s listed status only, i.e. a printout of the listing from the NSE’s website or a copy of the relevant page of the Financial Times.</p>
            <p>Enhanced Due Diligence (EDD)</p>
            <p>We are required to carry out Enhanced Due Diligence where there is a greater perceived risk of money laundering or terrorist financing. This requires us to take additional steps to understand the ownership and control of the client and, in some cases, the source of funds involved in the matter. There is also greater focus on ongoing monitoring.</p>
            <p>You must conduct EDD on:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>individual clients who you do not meet face-to-face</li>
                <li>Politically Exposed Persons (PEPs): these are persons entrusted, in the last year, with one of the following positions in a country outside Nigeria:heads of state, heads of government, ministers or deputy or assistant ministers:</li>
            </ul>
            <p>MPs</p>
            <p>judiciary whose decisions are not generally subject to further appeal</p>
            <p>members of courts of auditors or the boards of central banks</p>
            <p>Ambassadors</p>
            <p>high-ranking officers in the armed forces</p>
            <p>members of administrative, management or supervisory bodies of state-owned enterprises</p>
            <p>family members or close associates of the above</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li> It does not include middle-ranking or more junior individuals in these categories</li>
                <li>We also apply EDD to Nigeria PEPs on a risk sensitive basis. If you receive instructions from a Nigeria PEP please discuss the Client Due Diligence requirements with the Nominated Officer</li>
                <li>Other high risk clients: these are not defined and there are no prescribed measures that we are required to take. We have identified certain client types as high risk (see the Client Due Diligence Risk Tables (Appendix 6.3) and have set out the measures to be taken for each client type at “Identify and verify – what information or documents do I need to obtain from my client?” below)</li>
            </ul>
            <p>Regular Due Diligence (RDD)</p>
            <p>Regular due diligence applies where Simplified and Enhanced Due Diligence do not.</p>
            <p>Source of funds</p>
            <p>Understanding your client’s source of funds is an important step in the CDD process.</p>
            <p>When am I required to look into the source of funds in a transaction?</p>
            <p>You are not required to interrogate all clients about their entire financial history but you are required to take additional steps to ensure that the transaction is consistent with your knowledge of the client. This is part of the ongoing monitoring exercise which you must conduct on all matters; see further Ongoing monitoring below.</p>
            <p>You are required to establish the source of funds and source of wealth in every matter where you are acting for a Politically Exposed Person (PEP).</p>
            <p>What steps should I take?</p>
            <p>Scrutinising the source of funds is more than asking for the money to come from a bank account in the clients’ name. Your focus should be on understanding how the client can legitimately fund the transaction.</p>
            <p>For transactions involving PEPs you should consider whether there:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Are any warning signs of corruption</li>
                <li> Is any evidence that government or state funds are being used inappropriately</li>
            </ul>
            <p>Where a third party is providing funding to your client you may need to establish the source of funds. See “When can I accept funds from a third party?” below. You must document your investigations into the source of funds, including any questions asked, responses received and supporting evidence provided.</p>
            <p>If you have any concerns about the source of funds you must consider whether you need to submit an SAR to the Nominated Officer.</p>
            <p>CDD on beneficial owners</p>
            <p>CDD on beneficial owners is different from CDD on clients. You must:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Identify any beneficial owners, and then</li>
                <li>Validate their identity on a risk sensitive basis</li>
            </ul>
            <p>What is a beneficial owner?</p>
            <p>Where you are instructed by an agent or representative of an individual, the beneficial owner is the underlying individual on whose behalf the agent or representative is instructing you. Where you are instructed by a company, partnership or other body, the beneficial owner is as follows:</p>
            <p>Body corporate (including LLP) Any individual who:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>(For non-listed bodies) ultimately owns or controls more than 25% of the shares or voting rights of the body, or</li>
                <li>Otherwise exercises control over the management of the body</li>
               
            </ul>
            <p>Partnership (not LLP) Any individual who:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Ultimately is entitled to or controls more than a 25% share of the capital or profits of or more than 25% of the voting rights in the partnership, or</li>
                <li> Otherwise exercises control over the management of the partnership Trust </li>
                <li>Any individual who is entitled to a specified interest in at least 25% of the capital of the trust property</li>
                <li>The class of persons in whose main interest the trust is set up or operates (the class itself and not every member of the class), or</li>
                <li>Any individual who has control over the trust</li>
                
            </ul>
            <p>Other legal entity or arrangement ⦁ Any individual who benefits from at least 25% of the property of the entity or arrangement</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>The class of persons in whose main interest the entity or arrangement is set up or operates (the class itself and not every member of the class), or</li>
                <li>Any individual who exercises control over at least 25% of the property of the entity or arrangement</li>
                
            </ul>
            <p>Estate of a deceased person in the course of administration The executor or administrator</p>
            <p>Any other case The individual who ultimately owns or controls the client or on whose behalf a transaction is being conducted</p>
            <p>How do I conduct CDD on beneficial owners?</p>
            <p>You must first identify the beneficial owners. You can do this through a reliable public source (e.g. Companies House) or by asking the client. Unless there is any reason to doubt the information given you can rely on the client’s word. You must then consider the client’s risk profile, the structure of the business and the nature of the transaction. This will help you to decide what steps you need to take to verify the beneficial owner’s identity. In assessing the risk, you should consider:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Why your client is acting on behalf of someone else</li>
                <li>How well you know your client</li>
                <li>The type of business structure and its location</li>
                <li>The nature and risk profile of the matter</li>
            </ul>
            <p>The key is to understand the ownership and control of the client.</p>
            <p>The level of verification required will depend on your assessment of your client’s risk profile. When verifying the beneficial owner you can:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li> Look at organisation charts from the website, annual reports or the client</li>
                <li>Review the trust deed or partnership agreement</li>
                <li>Discuss beneficial ownership with the client and record the results of your discussion</li>
            </ul>
            <p>If the beneficial owner of a client is a company, you will need to establish the individual at the top of the corporate tree.</p>
            <p>What happens if I cannot conclude the CDD exercise?</p>
            <p>Where we are unable to apply CDD measures, the general rule is that we must:</p>
            <ul class="pl-5" style="list-style-type:disc">
                <li>Not carry out a transaction for the client</li>
                <li> Not establish a business relationship with a client</li>
                <li>Not accept funds from or transfer funds to a client or third party (see below: Receiving funds)</li>
                <li>Terminate any existing business relationship with the client</li>
                <li> Consider whether a SAR is required.</li>
            </ul>
            <p>There are very limited circumstances in which this may not apply, e.g. we may be able to verify the client’s identity during the establishment of a business relationship if this is necessary to avoid interrupting the normal course of business and there is little risk of money laundering–this is on condition that the verification is completed as soon as practicable after contact is first established.</p>
            <p>You must never unilaterally decide that it is acceptable to delay completion of CDD. If you are unable to apply or complete CDD on any matter, you should immediately seek advice from the Nominated Officer.</p>
            <p>Purpose and intended nature of the business relationship</p>
